def get_icon(fileName):
	fileName = fileName.lower()

	if "." in fileName:
		ext = fileName.split(".")[-1]
		if ext in icons:
			return icons[ext]
		else:
			return icons["unknown"]

	elif fileName in ["makefile", "copying", "license"]:
		return icons[fileName]
	else:
		return icons["unknown"]